/**
 * Port ABRD Tender Grid light-theme tokens into BOQ master list scope.
 * Source: d:/Brave/ABRD_Website/FrontEnd/src/app/shared/styles/app-theme-system.css
 * Target: styles/abrd-grid-tokens.css (.boq-master-list { ... })
 *
 * Run: node scripts/port-abrd-grid-tokens.mjs
 */
import fs from "node:fs";
import path from "node:path";

const abrdThemePath = "d:/Brave/ABRD_Website/FrontEnd/src/app/shared/styles/app-theme-system.css";
const outPath = "styles/abrd-grid-tokens.css";

const source = fs.readFileSync(abrdThemePath, "utf8");
const lightMatch = source.match(/html\[data-theme='light'\] \{([\s\S]*?)\n\}/);
if (!lightMatch) {
  throw new Error("Could not extract html[data-theme='light'] block from ABRD theme");
}

const lightBlock = lightMatch[1];
const gridTokenLines = lightBlock
  .split("\n")
  .map((line) => line.trim())
  .filter(
    (line) =>
      line.startsWith("--app-color-grid-") ||
      line.startsWith("--app-color-pill-") ||
      line.startsWith("--app-grid-pill-") ||
      line.startsWith("--app-grid-header-") ||
      line.startsWith("--primary:") ||
      line.startsWith("--bg0:") ||
      line.startsWith("--bg1:") ||
      line.startsWith("--surface:") ||
      line.startsWith("--border:") ||
      line.startsWith("--border-strong:") ||
      line.startsWith("--fg:") ||
      line.startsWith("--muted:"),
  );

const header = `/* ABRD Tender Grid tokens — scoped to BOQ master list (light theme parity)
 * Generated by scripts/port-abrd-grid-tokens.mjs — do not hand-edit token values.
 */
.boq-master-list {
  --data-grid-link-color: rgb(var(--primary));
  --data-grid-link-hover-color: color-mix(in oklab, rgb(var(--primary)) 88%, white 12%);
  --app-color-primary: rgb(var(--primary));
  --app-color-grid-link: var(--data-grid-link-color);
  --app-color-grid-link-hover: var(--data-grid-link-hover-color);
`;

const footer = `
  font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, "Helvetica Neue", Arial,
    sans-serif;
  color: rgb(var(--fg));
}
`;

fs.writeFileSync(
  path.join(process.cwd(), outPath),
  header + gridTokenLines.map((l) => "  " + l).join("\n") + footer,
);
console.log(`Wrote ${outPath} (${gridTokenLines.length} tokens from ABRD light theme)`);
